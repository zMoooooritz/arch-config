			require("conform").setup({
				notify_on_error = true,
				format_on_save = function(bufnr)
					if vim.b[bufnr].disable_autoformat then
						return
					end
					return {
						timeout_ms = 500,
						lsp_fallback = true,
					}
				end,
				formatters_by_ft = {
